/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
	if (__HAL_UART_GET_FLAG(&huart4, UART_FLAG_IDLE) != RESET&& huart4.hdmarx != NULL) 
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart4); // 清除标志

    HAL_UART_DMAStop(&huart4); // 停止 DMA

    // 计算长度 (注意：RX_BUF_SIZE 也要在这里定义或者直接写数字)
    Rx_len = 64 - __HAL_DMA_GET_COUNTER(huart4.hdmarx);
    flag = 1; 
		Rx_buffer[Rx_len] = '\0';
    HAL_UART_Receive_DMA(&huart4, Rx_buffer, 64); // 重新开启
  }
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

/**
  * @brief This function handles UART5 global interrupt.
  */
void UART5_IRQHandler(void)
{
  /* USER CODE BEGIN UART5_IRQn 0 */
	if (__HAL_UART_GET_FLAG(&huart5, UART_FLAG_IDLE) != RESET) 
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart5);
    
    HAL_UART_DMAStop(&huart5);
    // 获取实际收到的长度
    uint16_t rx_len = JY_BUF_SIZE - __HAL_DMA_GET_COUNTER(huart5.hdmarx);
    
    if(rx_len > 0) 
    {
        JY62_Decode_Buffer(&jy62, rx_len); 
    }

    // 重新从数组首地址 0 开始接收
    HAL_UART_Receive_DMA(&huart5, jy62.rx_fifo, JY_BUF_SIZE);
  }
  /* USER CODE END UART5_IRQn 0 */
  HAL_UART_IRQHandler(&huart5);
  /* USER CODE BEGIN UART5_IRQn 1 */

  /* USER CODE END UART5_IRQn 1 */
}

/**
  * @brief This function handles UART7 global interrupt.
  */
void UART7_IRQHandler(void)
{
  /* USER CODE BEGIN UART7_IRQn 0 */
if (__HAL_UART_GET_FLAG(&huart7, UART_FLAG_IDLE) != RESET && huart7.hdmarx != NULL)  
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart7); // 清除空闲中断标志位

    HAL_UART_DMAStop(&huart7); // 停止当前 DMA 接收

    AS_len = 10 - __HAL_DMA_GET_COUNTER(huart7.hdmarx);
    
    // 【👑 已移除 flag = 1 逻辑】

    // 边界安全检查：防止当正好接收满 10 字节时，写入 [10] 位置导致数组越界溢出
    if (AS_len < 10) 
    {
      AS_rx_buffer[AS_len] = '\0'; // 在数据末尾加上字符串结束符
    }

    // 重新开启下一次 DMA 接收，接收最大长度设为 10
    HAL_UART_Receive_DMA(&huart7, AS_rx_buffer, 10); 
  }
  /* USER CODE END UART7_IRQn 0 */
  HAL_UART_IRQHandler(&huart7);
  /* USER CODE BEGIN UART7_IRQn 1 */

  /* USER CODE END UART7_IRQn 1 */
}

